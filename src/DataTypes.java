public class DataTypes {
    public static void main(String[] args) {
        // Primitive Data Types
        // byte, short, int, float, double, char, boolean
        byte x = 2;
        short y = 3;
        int a = 10;
        long z = 1000000000000000000L;
        float b = 10.5f;
        double c = 10.5;
        char d = 'A';
        boolean e = true;

        System.out.println("The boolean variable is: "+e);
        System.out.println("The char variable is: "+d);
        System.out.println("The byte variable is: "+ x);
        System.out.println("The short variable is: "+ y);
        System.out.println("The int variable is: "+ a);
        System.out.println("The long variable is: "+ z);
        System.out.println("The float variable is: "+ b);
        System.out.println("The double variable is: "+ c);
    }
}
