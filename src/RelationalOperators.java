public class RelationalOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println("Value of a is equal to b: "+(a==b));
        System.out.println("Value of a is not equal to b: "+(a!=b));
        System.out.println("Value of a is greater than b: "+(a>b));
        System.out.println("Value of a is less than b: "+(a<b));
        System.out.println("Value of a is greater than or equal to b: "+(a>=b));
        System.out.println("Value of a is less than or equal to b: "+(a<=b));
    }
}
